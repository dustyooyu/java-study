package org.example.Week2.Day9_230427;

public class ComparisonOperatorsEx {
    public static void main(String[] args) {
        int iVal1 = 10;
        int iVal2 = 20;

        System.out.printf("iVal1 = %d, iVal2 = %d\n", iVal1, iVal2);
        System.out.printf("iVal1 == iVal2 --> %b\n", iVal1 == iVal2);
        System.out.printf("iVal1 != iVal2 --> %b\n", iVal1 != iVal2);
        System.out.printf("iVal1 < iVal2 --> %b\n", iVal1 < iVal2);
        System.out.printf("iVal1 <= iVal2 --> %b\n", iVal1 <= iVal2);
        System.out.printf("iVal1 > iVal2 --> %b\n", iVal1 > iVal2);
        System.out.printf("iVal1 >= iVal2 --> %b\n", iVal1 >= iVal2);
    }
}
