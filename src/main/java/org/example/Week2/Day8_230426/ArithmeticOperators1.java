package org.example.Week2.Day8_230426;

public class ArithmeticOperators1 {
    public static void main(String[] args) {
        int a = 3;
        int b = 10;

        System.out.printf("a + b = %d\n", a + b);
        System.out.printf("a - b = %d\n", a - b);
        System.out.printf("a * b = %d\n", a * b);
        System.out.printf("a / b = %d\n", a / b);
        System.out.printf("a %% b = %d\n", a % b);

    }
}
