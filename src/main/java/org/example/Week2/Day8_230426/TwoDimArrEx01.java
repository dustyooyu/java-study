package org.example.Week2.Day8_230426;

import java.util.Arrays;

public class TwoDimArrEx01 {
    public static void main(String[] args) {
        int[][] iArr1 = new int[3][3];
        System.out.println("iArr1 Arrays[3][3]");
        System.out.println(Arrays.toString(iArr1[0]));
        System.out.println(Arrays.toString(iArr1[1]));
        System.out.println(Arrays.toString(iArr1[2]));

        int[][] iArr2 = new int[10][10];
        System.out.println("iArr1 Arrays[10][10]");
        System.out.println(Arrays.toString(iArr2[0]));
        System.out.println(Arrays.toString(iArr2[1]));
        System.out.println(Arrays.toString(iArr2[2]));
        System.out.println(Arrays.toString(iArr2[3]));
        System.out.println(Arrays.toString(iArr2[4]));
        System.out.println(Arrays.toString(iArr2[5]));
        System.out.println(Arrays.toString(iArr2[6]));
        System.out.println(Arrays.toString(iArr2[7]));
        System.out.println(Arrays.toString(iArr2[8]));
        System.out.println(Arrays.toString(iArr2[9]));


    }
}
