package org.example.Week1.Day6_230424;

public class DivideIntoTwo {
    public static void main(String[] args) {
//      int와 float의 경계

        int fOne = 1;
        float fTwo = 2;

        int iOne = 1;
        int iTwo = 2;


        System.out.printf("int 1 + 1 / 2 = %d\n",iOne + iOne / iTwo);
        System.out.printf("float 1 + 1 / 2 = %f\n",fOne + fOne / fTwo);

        System.out.printf("9 / 2 = %d\n", 9 / 2);
        System.out.printf("9 / 2.0 = %f\n", 9 / 2.0);

    }
}
